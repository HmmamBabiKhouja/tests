function showWidth() {
  alert(
    window.screen.width * window.devicePixelRatio +
      " " +
      window.screen.width +
      "  " +
      window.devicePixelRatio
  );
}

function showHeight() {
  alert(
    window.screen.height * window.devicePixelRatio +
      " " +
      window.screen.height +
      "  " +
      window.devicePixelRatio
  );
}
